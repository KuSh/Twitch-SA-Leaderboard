  <h1 class="text-2xl font-semibold text-center my-8">
    Présentation des jeux du Jambionnat
  </h1>

  <section class="space-y-4">
    <ul class="list-disc list-inside space-y-4">
      <li>
        Battle Royale : Les BR se lancent en dépensant des points de chaîne
        (Hostiflards) pour la commande "Festival de Bourre-pifs !". Les avatars
        se battent entre eux automatiquement, et le dernier debout gagne un
        point pour le Jambionnat du mois en cours. Le fait de lurker peut vous
        aider à remporter des victoires !
      </li>
      <li>
        Basket : Chaque participant doit viser un panier en lançant son avatar
        depuis une étoile verte en choisissant un angle de visée et la force du
        tir (deux nombres à taper dans le tchat, séparés par un espace). Chacun
        pour soi ! La personne ayant marqué le plus de points à la fin du
        décompte, ou ayant atteint 7 points avant les autres, remporte trois
        points pour le Jambionnat du mois en cours. Pour participer, il suffit
        d'entrer les nombres associés à la visée et la force.
      </li>
      <li>
        Marbles on Stream : Chaque participant est représenté par une bille qui
        doit arriver au bout d'un circuit (plus ou moins tortueux) avant tout le
        monde. Tout se fait de manière automatique ! La personne ayant remporté
        le plus de victoires en fin de session (20 circuits) gagne cinq points
        pour le Jambionnat du mois suivant. Pour participer, il suffit de taper
        la commande " !play " dans le tchat avant le lancement de chaque course.
      </li>
      <li>
        Cacas volants : Périodiquement, un petit caca volant apparaîtra
        au-dessus des Avatars. Votre but est de l'attraper en utilisant les
        commandes d'action (jump ou sling) de l'extension StreamAvatars dispo à
        droite de l'écran du lecteur Twitch. Chaque caca attrapé rapporte un
        point qui ne compte pas pour le Jambionnat mais pour un score à part. En
        fin de mois, si vous avez attrapé plus de cacas que tout le monde et si
        vous n'avez pas remporté le Jambionnat mensuel, vous gagnez une commande
        à votre gloire de coprophage.
      </li>
    </ul>
  </section>
